# the classic :)
print("Hello World")

# using single quotes and double quotes with print
# if you need to use double quotes for something inside the string,
# use single quotes to mark where the string to be printed begins and ends
print('She said: "Hello" and then left.')

# the reverse works too
print("She said: 'Hello' and then left.")

# or alternatively, use the escape \ to mark which characters to ignore as code
print("She said: \"Hello\" and then left.")

# use \n for new line (enter/return)
print("Hello World\nHello World\nHello World")

